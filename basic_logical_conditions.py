a = 15
b = 25

# equals
equals = a == b
print(equals)

# not equals 
notequals = a != b
print(notequals)

# less than
lessthan = a < b
print(lessthan)

# less than or equal to
lessequalto = a <= b
print(lessequalto)

# greater than 
greaterthan = a > b
print(greaterthan)

# greater than or equal to 
greaterequalto = a >= b
print(greaterequalto)
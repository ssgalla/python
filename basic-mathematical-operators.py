# addition
numadd1 = 55
numadd2 = 45

addition = numadd1 + numadd2
print(addition)

# subtraction
numsub1 = 60
numsub2 = 30

subtract = numsub1 - numsub2
print(subtract)

# multiplication
nummulti1 = 25
nummulti2 = 5

multiplier = nummulti1 * nummulti2
print(multiplier)

# division
numdiv1 = 20
numdiv2 = 5

division = numdiv1 / numdiv2
print(division)

# exponential operator - finding the power of a number
numexp1 = 2
numexp2 = 10

exponential = numexp1 ** numexp2
print(exponential)

# modulo operator - find the remainder of a division sum
nummod1 = 4
nummod2 = 3

modulo = nummod1 % nummod2
print(modulo)

# floor division - returns the result of division sum to the nearest whole number (NO FLOATING NUMBER)
numfloor1 = 10
numfloor2 = 3

floor = numfloor1 // numfloor2
print(floor)
